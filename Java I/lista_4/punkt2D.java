public class punkt2D {

    private int x, y;

    public punkt2D(int startX, int startY){
        x = startX;
        y = startY;
    }

    public int getX(){
        return this.x;
    }

    public void setX(int x){
        this.x = x;
    }

    public int getY(){
        return this.y;
    }

    public void setY(int y){
        this.y = y;
    }

}
